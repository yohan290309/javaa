    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Agrega datos a la tabla:
        String datos [] = new String [2];
        datos [0] = txtNombre.getText();
        datos [1] = cmbGenero.getSelectedItem().toString();
        txtNombre.setText("");
        cmbGenero.setSelectedIndex(0);
        tabla.addRow(datos);
               
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Elimina datos de la tabla:
        int fila, opcion;
        fila = tblDatos.getSelectedRow();//selecciona fila
        opcion = JOptionPane.showConfirmDialog(null, "EstÃ¡ seguro de eliminar el registro "+fila,"Confirmar",0);
        //JOptionPane.showConfirmDialog(null, "Confirme eliminar", "Confirmar", 0, 1);
        if (fila>=0){
            if (opcion==0){
                tabla.removeRow(fila);
                JOptionPane.showMessageDialog(null, "Se ha eliminado el registro "+fila);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un registro en la tabla.");
        } 
       
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Modifica tabla:
        int fila;
        fila = tblDatos.getSelectedRow();//selecciona fila
        if (fila >=0){
            tabla.setValueAt(txtNombre.getText(), fila, 0);
            tabla.setValueAt(cmbGenero.getSelectedItem(), fila,1);
            txtNombre.setText("");
            cmbGenero.setSelectedIndex(0);
            JOptionPane.showMessageDialog(null, "Se ha modificado el registro "+fila);
        }else {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar un elemento ");
        } 
        
    }//GEN-LAST:event_jButton3ActionPerformed

